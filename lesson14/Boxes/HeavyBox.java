public class HeavyBox {
	private int size;
	private int weight;
	
	public HeavyBox(int size, int weight) {
		this.size = size;
		this.weight = weight;
	}
	
	public void setSize(int size) {
		this.size = size;
	}
	
	public int getSize() {
		return size;
	}
	
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	public int getWeight() {
		return weight;
	}
}
